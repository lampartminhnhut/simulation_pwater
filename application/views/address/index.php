Australia Post recommends[5] that the last line of the address should be set in capital letters. In Australia, subunits are essential and should be separated from the street by two spaces; apartments, flats and units are typically separated with a forward slash instead.<br>
As in the US, the state/territory is crucial information as many placenames are reused in different states/territories; it is usually separated from the suburb with two spaces and abbreviated. In printed matter, the postcode follows after two spaces; in handwritten matter, the postcode should be written in the boxes provided.<br>
