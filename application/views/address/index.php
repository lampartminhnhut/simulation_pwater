        In common with the English-speaking world, addresses in Australia put the street number—which may be a range—before the street name, and the placename before the postcode. <br>
Unlike addresses in most other comparable places, the city is not included in the address, but rather a much more fine-grained locality is used, usually referred to in Australia as a suburb or town – although these words are understood in a different way than in other countries. Because the suburb or town serves to locate the street or delivery type, the postcode serves only as routing information rather than to distinguish previous other parts of an address.<br>
As an example, there are around 8000 localities in Victoria (cf. List of localities in Victoria (Australia) and List of Melbourne suburbs), yet around 700 unique geographic postcodes.[4] For certain large volume receivers or post offices, the "locality" may be an institution or street name.<br>
It is always considered incorrect to include the city or metropolis name in an address (unless this happens to be the name of the suburb), and doing so may delay delivery.<br>
